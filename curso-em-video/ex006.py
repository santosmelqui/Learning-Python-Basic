'''Crie um programa que leia um número e mostre o seu dobro, triplo e a raiz quadrada'''

num = int(input("Digite um número para calcular o dobro, o triplo e a raiz quadrada: "))

dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)

print(f"O dobro é {dobro}, o triplo é {triplo} e a raiz quadrada é {raiz}")